'''
	[문제]
	    100 ~ 300 사이의 숫자 중에서 
     	[조건1] 8의 배수를 출력하고, 
      	[조건2] 그 총합을 구하시오.
		[조건3] 개수를 구하시오.
	[정답]
		104 112 120 128 136 144 152 160 168 176 184 192 200 208 216 224 232 240 248 256 264 272 280 288 296 
		total = 5000
		count = 25
'''

count = 0
total = 0

a = 100
while   a <= 300 :
	if a % 8 == 0 :
		print(a , end = " ")
		count += 1
		total += a
	a += 1
print()
print(total)
print(count)



