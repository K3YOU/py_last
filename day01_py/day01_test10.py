#대입연산자 (오른쪽의 값을 왼쪽으로 복사할때  사용한다)
# (1) a = 10
# (2) 위식의 =이 대입연산자이다
# (3) 변수는 오직 대입연산자를 통해서 값을 저장한다
# (4) =는 우리가 산수시간에 배운 같다가 아니다

a= 10
print(a+3) # 13
print(a)   # 10 (a의 값이 찐으로 바뀐거는 아니다)

b =20
b=b+3
print(b)


#변수의 값변경

num = 8
print(num +1) #9가 나오지만 num은 아직 8이다
print(num) #8

a = 10
a = a+1
print(a) #비로소 a는 11이 된다

#변수의 특징
#(1)변수의 값을 한개만 저장할수있다.
#(2) 새로운값이 저장되면 이전의 값은 사라진다


